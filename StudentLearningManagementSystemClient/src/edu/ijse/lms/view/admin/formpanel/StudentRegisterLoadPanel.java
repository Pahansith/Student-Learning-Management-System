    private void setValuesToField() {
        selectedRow = studentTable.getSelectedRow();
        
        if (selectedRow != -1) {
            String name = (String) studentTable.getValueAt(selectedRow, 0);
            String nic = (String) studentTable.getValueAt(selectedRow, 1);
            String batchNumber = Integer.toString((Integer) studentTable.getValueAt(selectedRow, 2));
            String telephone = Integer.toString((Integer) studentTable.getValueAt(selectedRow, 3));
            String dob = (String) studentTable.getValueAt(selectedRow, 4);
            String email = (String) studentTable.getValueAt(selectedRow, 5);
            String userName = (String) studentTable.getValueAt(selectedRow, 6);
            
            
            nameField.setText(name);
            nicField.setText(nic);
            telephoneField.setText(telephone);
            dobField.setText(dob);
            emailField.setText(email);
            userNameField.setText(userName);
            batchField.setText(batchNumber);
        }
    }


