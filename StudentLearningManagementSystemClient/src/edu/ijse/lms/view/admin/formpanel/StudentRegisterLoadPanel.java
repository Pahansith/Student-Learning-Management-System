    public void setStudents(LinkedList<StudentRegInfo> allRegisterPendingStudent) {
        this.registerPendingStudentList = allRegisterPendingStudent;
        DefaultTableModel model = (DefaultTableModel) studentTable.getModel();
        model.setRowCount(0);
        for (StudentRegInfo reg : allRegisterPendingStudent) {
            Object rowData[] = {reg.getfName()+" "+reg.getlName(),reg.getNic(),reg.getBatch().getBatchNumber(),reg.getTelephone(),reg.getDob(),reg.getEmail(),reg.getUserName()};
            model.addRow(rowData);
        }
    }

    @Override
    public void update() {
        JOptionPane.showMessageDialog(this, "New Student Registered", "New Registration", JOptionPane.INFORMATION_MESSAGE);
        setTableValues();
    }
